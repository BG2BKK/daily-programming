// s must be [01]{1,64}
std::string bin2dec(const std::string& s) {
    assert(s.length() >= 1 && s.length() <= 64);
    uint8_t sum[19] = { 0 };
    for (int i = 0; i < s.length(); i++)
        if (s[i] == '1')
            for (int j = 0; j < 19; j++) {
                sum[j] += weight[(s.length() - 1 - i) * 19 + j];
                if (sum[j] >= 10)
                    sum[j] -= 10;
            }
        else
            assert(s[i] == '0');
    
    // First non-zero digit
    int k = 18;
    while (k > 0 && sum[k] == 0)
        k--;

    // reverse output
    std::string o;
    o.reserve(k + 1);
    for (int j = 0; j <= k; j++)
        o.push_back(sum[k - j] + '0');
    return o;
}

